		<h2 id="bruk">Hvordan kan du bruke denne nettsiden sammen med barnet ditt?</h2>
		<p> Begynn gjerne med å se på bildet og snakk med barnet ditt på det språket du kan best om hva dere ser på bildet. Hva tror dere dette kan handle om, har dere sett noen av disse dyra før, eventuelt hvor, hvordan så de/den ut osv.</p>
		<p>Gå så til den lange fortellingen og trykk på de språkene dere ønsker å se teksten/lytte til fortellingen på. Lytt til historien og følg gjerne med i teksten. Eller kanskje kan du lese historien for barnet ditt? Stopp gjerne underveis for å snakke om ord som er vanskelige eller ting som dere kommer på er naturlig å snakke om. Etter dere har hørt den lange fortellingen, så snakk med barnet om hva de tror vil skje videre. Hva skal de gjøre i mørket? Hva ville du ha gjort hvis du kunne gjøre deg selv så liten eller så stor du ville? Tenk om du var med i meitemarkens ganger. Hva ville du ha vært mest redd for? Hva ville vært mest morsomt?</p>
		<p>Trykk så på den korte fortellingen. Denne teksten er ment som en tekst barnet kan lese selv. La barnet selv velge hvilket språk det vil lese på. Be barnet lese for deg eller les den korte teksten sammen.</p>
		<p>Trykk så på oppgaver og gjør kvissen for å sjekke hva dere husker av det dere har lest/lyttet til. Spør barnet hvilke av de andre oppgavene det har lyst til å gjøre.</p>
		<p>Hvis dere trykker på knappen filmer finner du lenker til filmer som handler om meitemarken.</p>
